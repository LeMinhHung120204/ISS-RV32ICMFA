    .section .text
    .globl _start
    .option norvc

_start:
    ####################################################################
    # Shift group test for RV32I - TEST CASE 01
    # Tested instructions:
    #   sll, slli, srl, srli, sra, srai
    ####################################################################

    ####################################################################
    # TEST 1: sll 1 << 1 = 2
    ####################################################################
    li s0, 1
    li t0, 1
    li t1, 1
    sll  t2, t0, t1
    li t3, 2
    bne  t2, t3, fail

    ####################################################################
    # TEST 2: sll 1 << 31 = 0x80000000
    ####################################################################
    li s0, 2
    li t0, 1
    li t1, 31
    sll  t2, t0, t1
    lui  t3, 0x80000
    bne  t2, t3, fail

    ####################################################################
    # TEST 3: slli 1 << 4 = 16
    ####################################################################
    li s0, 3
    li t0, 1
    slli t1, t0, 4
    li t2, 16
    bne  t1, t2, fail

    ####################################################################
    # TEST 4: slli with shamt 0 - unchanged
    ####################################################################
    li s0, 4
    li t0, 7
    slli t1, t0, 0
    bne  t1, t0, fail

    ####################################################################
    # TEST 5: srl 0x80000000  1 = 0x40000000
    ####################################################################
    li s0, 5
    lui  t0, 0x80000
    li t1, 1
    srl  t2, t0, t1
    lui  t3, 0x40000
    bne  t2, t3, fail

    ####################################################################
    # TEST 6: srl 0xffffffff  1 = 0x7fffffff
    ####################################################################
    li s0, 6
    li t0, -1
    li t1, 1
    srl  t2, t0, t1
    lui  t3, 0x80000
    addi t3, t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 7: srli 16  4 = 1
    ####################################################################
    li s0, 7
    li t0, 16
    srli t1, t0, 4
    li t2, 1
    bne  t1, t2, fail

    ####################################################################
    # TEST 8: sra 0xffffffff  1 = 0xffffffff
    ####################################################################
    li s0, 8
    li t0, -1
    li t1, 1
    sra  t2, t0, t1
    li t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 9: sra 0x80000000  1 = 0xc0000000
    ####################################################################
    li s0, 9
    lui  t0, 0x80000
    li t1, 1
    sra  t2, t0, t1
    lui  t3, 0xc0000
    bne  t2, t3, fail

    ####################################################################
    # TEST 10: srai -8  2 = -2
    ####################################################################
    li s0, 10
    li t0, -8
    srai t1, t0, 2
    li t2, -2
    bne  t1, t2, fail

    ####################################################################
    # TEST 11: srai with shamt 0 - unchanged
    ####################################################################
    li s0, 11
    li t0, -5
    srai t1, t0, 0
    bne  t1, t0, fail

    ####################################################################
    # TEST 12: shift amount uses low 5 bits only (1 << 32 == 1 << 0)
    ####################################################################
    li s0, 12
    li t0, 1
    li t1, 32
    sll  t2, t0, t1
    bne  t2, t0, fail

pass:
    li a0, 1
pass_loop:
    jal x0, pass_loop

fail:
    add  a0, s0, x0
fail_loop:
    jal x0, fail_loop
