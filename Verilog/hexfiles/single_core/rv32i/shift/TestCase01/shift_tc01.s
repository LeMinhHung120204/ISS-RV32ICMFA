<FILE filename="shift_tc01.s" size="4144 bytes">
    .section .text
    .globl _start
    .option norvc

_start:
    ####################################################################
    # Shift group test for RV32I - TEST CASE 01
    # Tested instructions:
    #   sll, slli, srl, srli, sra, srai
    ####################################################################

    ####################################################################
    # TEST 1: sll 1 << 1 = 2
    ####################################################################
    addi s0, x0, 1
    addi t0, x0, 1
    addi t1, x0, 1
    sll  t2, t0, t1
    addi t3, x0, 2
    bne  t2, t3, fail

    ####################################################################
    # TEST 2: sll 1 << 31 = 0x80000000
    ####################################################################
    addi s0, x0, 2
    addi t0, x0, 1
    addi t1, x0, 31
    sll  t2, t0, t1
    lui  t3, 0x80000
    bne  t2, t3, fail

    ####################################################################
    # TEST 3: slli 1 << 4 = 16
    ####################################################################
    addi s0, x0, 3
    addi t0, x0, 1
    slli t1, t0, 4
    addi t2, x0, 16
    bne  t1, t2, fail

    ####################################################################
    # TEST 4: slli with shamt 0 -> unchanged
    ####################################################################
    addi s0, x0, 4
    addi t0, x0, 7
    slli t1, t0, 0
    bne  t1, t0, fail

    ####################################################################
    # TEST 5: srl 0x80000000 >> 1 = 0x40000000
    ####################################################################
    addi s0, x0, 5
    lui  t0, 0x80000
    addi t1, x0, 1
    srl  t2, t0, t1
    lui  t3, 0x40000
    bne  t2, t3, fail

    ####################################################################
    # TEST 6: srl 0xffffffff >> 1 = 0x7fffffff
    ####################################################################
    addi s0, x0, 6
    addi t0, x0, -1
    addi t1, x0, 1
    srl  t2, t0, t1
    lui  t3, 0x80000
    addi t3, t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 7: srli 16 >> 4 = 1
    ####################################################################
    addi s0, x0, 7
    addi t0, x0, 16
    srli t1, t0, 4
    addi t2, x0, 1
    bne  t1, t2, fail

    ####################################################################
    # TEST 8: sra 0xffffffff >> 1 = 0xffffffff
    ####################################################################
    addi s0, x0, 8
    addi t0, x0, -1
    addi t1, x0, 1
    sra  t2, t0, t1
    addi t3, x0, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 9: sra 0x80000000 >> 1 = 0xc0000000
    ####################################################################
    addi s0, x0, 9
    lui  t0, 0x80000
    addi t1, x0, 1
    sra  t2, t0, t1
    lui  t3, 0xc0000
    bne  t2, t3, fail

    ####################################################################
    # TEST 10: srai -8 >> 2 = -2
    ####################################################################
    addi s0, x0, 10
    addi t0, x0, -8
    srai t1, t0, 2
    addi t2, x0, -2
    bne  t1, t2, fail

    ####################################################################
    # TEST 11: srai with shamt 0 -> unchanged
    ####################################################################
    addi s0, x0, 11
    addi t0, x0, -5
    srai t1, t0, 0
    bne  t1, t0, fail

    ####################################################################
    # TEST 12: shift amount uses low 5 bits only (1 << 32 == 1 << 0)
    ####################################################################
    addi s0, x0, 12
    addi t0, x0, 1
    addi t1, x0, 32
    sll  t2, t0, t1
    bne  t2, t0, fail

pass:
    addi a0, x0, 1
pass_loop:
    jal x0, pass_loop

fail:
    add  a0, s0, x0
fail_loop:
    jal x0, fail_loop
