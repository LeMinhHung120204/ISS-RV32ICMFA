    .section .text
    .globl _start
    .option norvc

_start:
    ####################################################################
    # Memory group test for RV32I - TEST CASE 01
    # Tested instructions:
    #   lb, lbu, lh, lhu, lw, sb, sh, sw
    ####################################################################

    la   s1, test_mem

    ####################################################################
    # TEST 1: sw/lw basic
    ####################################################################
    addi s0, x0, 1
    lui  t0, 0x12345
    addi t0, t0, 0x678
    sw   t0, 0(s1)
    lw   t1, 0(s1)
    bne  t0, t1, fail

    ####################################################################
    # TEST 2: sb + lbu
    ####################################################################
    addi s0, x0, 2
    addi t0, x0, 0x7f
    sb   t0, 4(s1)
    lbu  t1, 4(s1)
    bne  t0, t1, fail

    ####################################################################
    # TEST 3: sb 0xff + lb sign extend -> -1
    ####################################################################
    addi s0, x0, 3
    addi t0, x0, -1
    sb   t0, 5(s1)
    lb   t1, 5(s1)
    addi t2, x0, -1
    bne  t1, t2, fail

    ####################################################################
    # TEST 4: sb 0xff + lbu zero extend -> 255
    ####################################################################
    addi s0, x0, 4
    lbu  t1, 5(s1)
    addi t2, x0, 255
    bne  t1, t2, fail

    ####################################################################
    # TEST 5: sh + lhu
    ####################################################################
    addi s0, x0, 5
    lui  t0, 0x0
    addi t0, t0, 0x234
    slli t0, t0, 4
    addi t0, t0, 0x5
    sh   t0, 6(s1)
    lhu  t1, 6(s1)
    bne  t1, t0, fail

    ####################################################################
    # TEST 6: sh 0xffff + lh sign extend -> -1
    ####################################################################
    addi s0, x0, 6
    addi t0, x0, -1
    sh   t0, 8(s1)
    lh   t1, 8(s1)
    addi t2, x0, -1
    bne  t1, t2, fail

    ####################################################################
    # TEST 7: sh 0xffff + lhu zero extend -> 65535
    ####################################################################
    addi s0, x0, 7
    lhu  t1, 8(s1)
    lui  t2, 0x10
    addi t2, t2, -1
    bne  t1, t2, fail

    ####################################################################
    # TEST 8: byte offsets inside same word
    ####################################################################
    addi s0, x0, 8
    addi t0, x0, 0x11
    addi t1, x0, 0x22
    addi t2, x0, 0x33
    addi t3, x0, 0x44
    sb   t0, 12(s1)
    sb   t1, 13(s1)
    sb   t2, 14(s1)
    sb   t3, 15(s1)
    lw   t4, 12(s1)
    lui  t5, 0x44332
    addi t5, t5, 0x211
    bne  t4, t5, fail

    ####################################################################
    # TEST 9: word overwrite by sw
    ####################################################################
    addi s0, x0, 9
    lui  t0, 0xa5a5a
    addi t0, t0, 0x5a5
    sw   t0, 16(s1)
    lw   t1, 16(s1)
    bne  t0, t1, fail

    ####################################################################
    # TEST 10: x0 store source behaves as zero
    ####################################################################
    addi s0, x0, 10
    sw   x0, 20(s1)
    lw   t0, 20(s1)
    bne  t0, x0, fail

    ####################################################################
    # TEST 11: negative byte sign extension
    ####################################################################
    addi s0, x0, 11
    addi t0, x0, -128
    sb   t0, 24(s1)
    lb   t1, 24(s1)
    addi t2, x0, -128
    bne  t1, t2, fail

    ####################################################################
    # TEST 12: negative half sign extension
    ####################################################################
    addi s0, x0, 12
    lui  t0, 0xfffff
    addi t0, t0, 0x800
    sh   t0, 26(s1)
    lh   t1, 26(s1)
    lui  t2, 0xfffff
    addi t2, t2, 0x800
    bne  t1, t2, fail

pass:
