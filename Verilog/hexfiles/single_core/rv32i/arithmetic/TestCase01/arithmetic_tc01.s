    .section .text
    .globl _start
    .option norvc

__start:
    ####################################################################
    # Arithmetic group test for RV32I - TEST CASE 01
    # Tested instructions: add, addi, sub, lui, auipc
    ####################################################################

    ####################################################################
    # TEST 1: add 0 + 0 = 0
    ####################################################################
    li s0, 1
    li t0, 0
    li t1, 0
    add  t2, t0, t1
    bne  t2, x0, fail

    ####################################################################
    # TEST 2: add 1 + 1 = 2
    ####################################################################
    li s0, 2
    li t0, 1
    li t1, 1
    add  t2, t0, t1
    li t3, 2
    bne  t2, t3, fail

    ####################################################################
    # TEST 3: add 3 + 7 = 10
    ####################################################################
    li s0, 3
    li t0, 3
    li t1, 7
    add  t2, t0, t1
    li t3, 10
    bne  t2, t3, fail

    ####################################################################
    # TEST 4: add -1 + 1 = 0
    ####################################################################
    li s0, 4
    li t0, -1
    li t1, 1
    add  t2, t0, t1
    bne  t2, x0, fail

    ####################################################################
    # TEST 5: add -1 + -1 = -2
    ####################################################################
    li s0, 5
    li t0, -1
    li t1, -1
    add  t2, t0, t1
    li t3, -2
    bne  t2, t3, fail

    ####################################################################
    # TEST 6: add 0x7fffffff + 1 = 0x80000000 (wrap)
    ####################################################################
    li s0, 6
    lui  t0, 0x80000
    addi t0, t0, -1
    li t1, 1
    add  t2, t0, t1
    lui  t3, 0x80000
    bne  t2, t3, fail

    ####################################################################
    # TEST 7: add 0x80000000 + (-1) = 0x7fffffff (wrap)
    ####################################################################
    li s0, 7
    lui  t0, 0x80000
    li t1, -1
    add  t2, t0, t1
    lui  t3, 0x80000
    addi t3, t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 8: addi 0 + 0 = 0
    ####################################################################
    li s0, 8
    li t0, 0
    addi t1, t0, 0
    bne  t1, x0, fail

    ####################################################################
    # TEST 9: addi 5 + 7 = 12
    ####################################################################
    li s0, 9
    li t0, 5
    addi t1, t0, 7
    li t2, 12
    bne  t1, t2, fail

    ####################################################################
    # TEST 10: addi 5 + (-3) = 2
    ####################################################################
    li s0, 10
    li t0, 5
    addi t1, t0, -3
    li t2, 2
    bne  t1, t2, fail

    ####################################################################
    # TEST 11: addi -1 + 1 = 0
    ####################################################################
    li s0, 11
    li t0, -1
    addi t1, t0, 1
    bne  t1, x0, fail

    ####################################################################
    # TEST 12: addi immediate max  2047
    ####################################################################
    li s0, 12
    li t0, 0
    addi t1, t0, 2047
    lui  t2, 0x1
    addi t2, t2, -1
    bne  t1, t2, fail

    ####################################################################
    # TEST 13: addi immediate min -2048
    ####################################################################
    li s0, 13
    li t0, 0
    addi t1, t0, -2048
    lui  t2, 0xfffff
    addi t2, t2, 2047
    addi t2, t2, 1     # 2047 + 1 = 2048
    bne  t1, t2, fail

    ####################################################################
    # TEST 14: addi 0x7fffffff + (-1) = 0x7ffffffe
    ####################################################################
    li s0, 14
    lui  t0, 0x80000
    addi t0, t0, -1
    addi t1, t0, -1
    lui  t2, 0x80000
    addi t2, t2, -2
    bne  t1, t2, fail

    ####################################################################
    # TEST 15: x0 must remain zero after addi to x0
    ####################################################################
    li s0, 15
    li t0, 0
    li x0, 123
    bne  x0, t0, fail

    ####################################################################
    # TEST 16: sub 0 - 0 = 0
    ####################################################################
    li s0, 16
    li t0, 0
    li t1, 0
    sub  t2, t0, t1
    bne  t2, x0, fail

    ####################################################################
    # TEST 17: sub 7 - 3 = 4
    ####################################################################
    li s0, 17
    li t0, 7
    li t1, 3
    sub  t2, t0, t1
    li t3, 4
    bne  t2, t3, fail

    ####################################################################
    # TEST 18: sub 0 - 1 = -1
    ####################################################################
    li s0, 18
    li t0, 0
    li t1, 1
    sub  t2, t0, t1
    li t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 19: sub -1 - 1 = -2
    ####################################################################
    li s0, 19
    li t0, -1
    li t1, 1
    sub  t2, t0, t1
    li t3, -2
    bne  t2, t3, fail

    ####################################################################
    # TEST 20: sub 0x80000000 - 1 = 0x7fffffff (wrap)
    ####################################################################
    li s0, 20
    lui  t0, 0x80000
    li t1, 1
    sub  t2, t0, t1
    lui  t3, 0x80000
    addi t3, t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 21: lui 0x00000 - 0x00000000
    ####################################################################
    li s0, 21
    lui  t0, 0x00000
    bne  t0, x0, fail

    ####################################################################
    # TEST 22: lui 0x12345 - 0x12345000
    ####################################################################
    li s0, 22
    lui  t0, 0x12345
    lui  t1, 0x12345
    bne  t0, t1, fail

    ####################################################################
    # TEST 23: lui 0xfffff - 0xfffff000
    ####################################################################
    li s0, 23
    lui  t0, 0xfffff
    lui  t1, 0xfffff
    bne  t0, t1, fail

    ####################################################################
    # TEST 24: lui 0x80000 - 0x80000000
    ####################################################################
    li s0, 24
    lui  t0, 0x80000
    lui  t1, 0x80000
    bne  t0, t1, fail

    ####################################################################
    # TEST 25: auipc 0 - current PC
    ####################################################################
    li s0, 25
auipc_zero_0:
    auipc t0, 0
    auipc t1, 0
    addi  t0, t0, 4
    bne   t0, t1, fail

    ####################################################################
    # TEST 26: auipc +1 - PC + 0x1000
    ####################################################################
    li s0, 26
auipc_pos_0:
    auipc t0, 1
    auipc t1, 0
    lui   t2, 0x1
    add   t1, t1, t2
    addi  t1, t1, -4
    bne   t0, t1, fail

    ####################################################################
    # TEST 27: auipc -1 - PC - 0x1000
    ####################################################################
    li s0, 27
auipc_neg_0:
    auipc t0, 0xfffff
    auipc t1, 0
    lui   t2, 0xfffff
    add   t1, t1, t2
    addi  t1, t1, -4
    bne   t0, t1, fail

pass:
    li a0, 1
pass_loop:
    jal x0, pass_loop

fail:
    add  a0, s0, x0
fail_loop:
    jal x0, fail_loop
