.section .text
.globl _start
.option norvc

_start:
    ####################################################################
    # Arithmetic group test for RV32I - TEST CASE 07
    # Extra edge cases: 0x5555AAAA mix, max positive wrap
    ####################################################################

    ####################################################################
    # TEST 1: add 0x5555AAAA + 0xAAAA5555 = 0xFFFFFFFF
    ####################################################################
    li s0, 1
    lui  t0, 0x5555a
    addi t0, t0, 0xaaa
    lui  t1, 0xaaaa5
    addi t1, t1, 0x555
    add  t2, t0, t1
    li   t3, -1
    bne  t2, t3, fail

    ####################################################################
    # TEST 2: add 0x7fffffff + 1 = 0x80000000 (wrap)
    ####################################################################
    li s0, 2
    lui  t0, 0x80000
    addi t0, t0, -1
    li   t1, 1
    add  t2, t0, t1
    lui  t3, 0x80000
    bne  t2, t3, fail

    ####################################################################
    # TEST 3: sub max - max = 0
    ####################################################################
    li s0, 3
    li   t0, 0x7fffffff
    sub  t1, t0, t0
    bne  t1, x0, fail

    ####################################################################
    # TEST 4: add -1 + -1 = -2
    ####################################################################
    li s0, 4
    li   t0, -1
    addi t1, t0, -1
    addi t2, t1, -1
    li   t3, -3
    bne  t2, t3, fail

    ####################################################################
    # TEST 5: auipc test
    ####################################################################
    li s0, 5
    auipc t0, 0x100
    auipc t1, 0
    lui  t2, 0x100
    add  t1, t1, t2
    addi t1, t1, -4
    bne  t0, t1, fail

    ####################################################################
    # TEST 6: addi max positive chained
    ####################################################################
    li s0, 6
    li   t0, 2047
    addi t1, t0, 2047
    addi t2, t1, 2047
    lui  t3, 0x1
    addi t3, t3, 2045
    bne  t2, t3, fail

    ####################################################################
    # TEST 7: x0 must always be zero
    ####################################################################
    li s0, 7
    li   t0, 0
    bne  x0, t0, fail

    ####################################################################
    # TEST 8: addi overflow to zero
    ####################################################################
    li s0, 8
    lui  t0, 0xfffff
    addi t1, t0, 2048
    bne  t1, x0, fail

    ####################################################################
    # TEST 9: sub negative number
    ####################################################################
    li s0, 9
    li   t0, 0
    addi t1, t0, -1
    sub  t2, t1, t0
    bne  t2, t1, fail

    ####################################################################
    # TEST 10: add large positive number
    ####################################################################
    li s0, 10
    lui  t0, 0x40000
    add  t1, t0, t0
    lui  t2, 0x80000
    bne  t1, t2, fail

pass:
    li a0, 1
pass_loop:
    jal x0, pass_loop

fail:
    add  a0, s0, x0
fail_loop:
    jal x0, fail_loop