    .section .text
    .globl _start
    .option norvc

_start:
    ####################################################################
    # Logical group test for RV32I - TEST CASE 01
    # Tested instructions:
    #   and, andi, or, ori, xor, xori
    ####################################################################

    ####################################################################
    # TEST 1: and 0xf0f0f0f0 & 0x0ff00ff0 = 0x00f000f0
    ####################################################################
    addi s0, x0, 1
    lui  t0, 0xf0f0f
    addi t0, t0, 0x0f0
    lui  t1, 0x0ff01
    addi t1, t1, -16
    and  t2, t0, t1
    lui  t3, 0x00f00
    addi t3, t3, 0x0f0
    bne  t2, t3, fail

    ####################################################################
    # TEST 2: andi 0x12345678 & 0x7ff(low 12) = 0x678
    ####################################################################
    addi s0, x0, 2
    lui  t0, 0x12345
    addi t0, t0, 0x678
    andi t1, t0, 0x7ff
    lui  t2, 0x0
    addi t2, t2, 0x678
    bne  t1, t2, fail

    ####################################################################
    # TEST 3: or 0xf0f0f0f0 | 0x0ff00ff0 = 0xfff0fff0
    ####################################################################
    addi s0, x0, 3
    lui  t0, 0xf0f0f
    addi t0, t0, 0x0f0
    lui  t1, 0x0ff01
    addi t1, t1, -16
    or   t2, t0, t1
    lui  t3, 0xfff10
    addi t3, t3, -16
    bne  t2, t3, fail

    ####################################################################
    # TEST 4: ori 0x12345000 | 0x678 = 0x12345678
    ####################################################################
    addi s0, x0, 4
    lui  t0, 0x12345
    ori  t1, t0, 0x678
    lui  t2, 0x12345
    addi t2, t2, 0x678
    bne  t1, t2, fail

    ####################################################################
    # TEST 5: xor equal values -> 0
    ####################################################################
    addi s0, x0, 5
    lui  t0, 0x13579
    addi t0, t0, -0x135
    xor  t1, t0, t0
    bne  t1, x0, fail

    ####################################################################
    # TEST 6: xori with -1 -> bitwise not
    ####################################################################
    addi s0, x0, 6
    lui  t0, 0x12345
    addi t0, t0, 0x678
    xori t1, t0, -1
    lui  t2, 0xedcba
    addi t2, t2, -0x679
    bne  t1, t2, fail

    ####################################################################
    # TEST 7: and with zero -> zero
    ####################################################################
    addi s0, x0, 7
    lui  t0, 0x7ffff
    addi t0, t0, -1
    and  t1, t0, x0
    bne  t1, x0, fail

    ####################################################################
    # TEST 8: or with zero -> self
    ####################################################################
    addi s0, x0, 8
    lui  t0, 0x54321
    addi t0, t0, 0x123
    or   t1, t0, x0
    bne  t1, t0, fail

    ####################################################################
    # TEST 9: xor with zero -> self
    ####################################################################
    addi s0, x0, 9
    lui  t0, 0x22222
    addi t0, t0, 0x222
    xor  t1, t0, x0
    bne  t1, t0, fail

    ####################################################################
    # TEST 10: x0 must remain zero
    ####################################################################
    addi s0, x0, 10
    lui  t0, 0x12345
    and  x0, t0, t0
    bne  x0, x0, fail

pass:
    addi a0, x0, 1
pass_loop:
    jal x0, pass_loop

fail:
    add  a0, s0, x0
fail_loop:
    jal x0, fail_loop
